package com.sda.j2.examples.zad11;

public class MoveDirection {
    private final double x;
    private final double y;

    public MoveDirection(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
